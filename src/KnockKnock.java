import javax.swing.JOptionPane;

public class KnockKnock {
	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null, "Here's a really bad knock knock joke:");
		JOptionPane.showMessageDialog(null, "Knock knock!");
		JOptionPane.showMessageDialog(null, "Now you're supposed to ask who's there.");
		JOptionPane.showMessageDialog(null, "The response: A cow goes.");
		JOptionPane.showMessageDialog(null, "Clearly confused, you say, 'A cow goes who?'");
		JOptionPane.showMessageDialog(null, "The response: No, a cow goes moo!");
		JOptionPane.showMessageDialog(null, "It's so bad, it's funny.");
	}
}
